package com.modal;

public class AdminUser{
	private int adminUserID;
	private String adminUserName;
	private String adminUserEmail;
	private String adminUserOccupation;
	private String adminUserContactNo;
	private String adminUserPassword;

    
	public int getAdminUserID() {
		return adminUserID;
	}

	public void setAdminUserID(int adminUserID) {
		this.adminUserID = adminUserID;
	}

	public String getAdminUserName() {
		return adminUserName;
	}

	public void setAdminUserName(String adminUserName) {
		this.adminUserName = adminUserName;
	}

	public String getAdminUserEmail() {
		return adminUserEmail;
	}

	public void setAdminUserEmail(String adminUserEmail) {
		this.adminUserEmail = adminUserEmail;
	}
	
	

	public String getAdminUserOccupation() {
		return adminUserOccupation;
	}

	public void setAdminUserOccupation(String adminUserOccupation) {
		this.adminUserOccupation = adminUserOccupation;
	}

	public String getAdminUserContactNo() {
		return adminUserContactNo;
	}

	public void setAdminUserContactNo(String adminUserContactNo) {
		this.adminUserContactNo = adminUserContactNo;
	}

	public String getAdminUserPassword() {
		return adminUserPassword;
	}

	public void setAdminUserPassword(String adminUserPassword) {
		this.adminUserPassword = adminUserPassword;
	}


}
